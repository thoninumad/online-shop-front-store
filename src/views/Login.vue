<template>
    <v-card class="main">
        <v-toolbar color="#fff">
            <v-btn icon  @click="close">
                <v-icon>close</v-icon>
            </v-btn>
            <v-toolbar-title>Login dan Mulai Berbelanja!</v-toolbar-title>
        </v-toolbar>
        <v-divider></v-divider>

        <v-container fluid>
            <v-form ref="form" v-model="valid" lazy-validation>
                <v-text-field v-model="email" :rules="emailRules" label="E-mail" required append-icon="email"></v-text-field>
                <v-text-field v-model="password" :append-icon="showPassword ? 'visibility' : 'visibility_off'"
                :rules="passwordRules" :type="showPassword ? 'text' : 'password'" label="Password" hint="At least 6 characters"
                counter @click:append="showPassword = !showPassword"></v-text-field>

                <div class="text-xs-center">
                    <v-btn large color="#bd1544" :disabled="!valid" @click="submit" class="white--text">
                        Login <v-icon right dark>lock_open</v-icon>
                    </v-btn>
                </div>
            </v-form>
        </v-container>
    </v-card>
</template>

<style lang="css">
  @import '/css/main.css';
</style>

<script>
    import { mapGetters, mapActions } from 'vuex'
    export default {
        name : 'login',
        data () {
            return {
                valid : true,
                email : 'lou56@example.org',
                emailRules : [
                    v => !!v || 'E-mail is required',
                    v => /([a-zA-Z0-9_]{1,})(@)([a-zA-Z0-9_]{2,}).([a-zA-Z0-9_]{2,})+/.test(v) || 'E-mail must be valid'
                ],
                showPassword : false,
                password : '',
                passwordRules : [
                    v => !!v || 'Password required.',
                    v => (v && v.length >= 6) || 'Min 6 characters',
                ],
            }
        },
        computed : {
            ...mapGetters({
                user : 'auth/user',
                prevUrl : 'prevUrl',
            }),
        },
        methods : {
            ...mapActions({
                setAlert : 'alert/set',
                setStatusDialog : 'dialog/setStatus',
                setAuth : 'auth/set',
            }),
            close() {
                this.setStatusDialog(false)
            },
            submit() {
                if(this.$refs.form.validate()) {
                    let formData = {
                        'email' : this.email,
                        'password' : this.password
                    }
                    this.axios.post('/login', formData)
                      .then((response) => {
                          let data_user = response.data.data
                          this.setAuth(data_user)
                          if(this.user.id>0) {
                              this.setAlert({
                                  status : true,
                                  text : 'Login success',
                                  type : 'success',
                              })

                              if(this.prevUrl.length>0) this.$router.push(this.prevUrl)
                              this.close();

                          } else {
                              this.setAlert({
                                  status : true,
                                  text : 'Login error',
                                  type : 'error',
                              })
                          }
                      })
                      .catch((error) => {
                          let responses = error.response
                          this.setAlert({
                              status : true,
                              text : responses.data.message,
                              type : 'error',
                          })
                      })
                }
            },
        },
    }
</script>
